class Player {

    constructor(name, stash, bet, hasChecked, hasFold, hasRaised, hasCalled, hand) {

        this.name = name;
        this.stash = stash;
        this.bet = bet
        this.hasChecked = hasChecked;
        this.hasFold = hasFold;
        this.hasRaised = hasRaised;
        this.hasCalled = hasCalled;
        this.hand = hand;
    }

}